def import_curriculum():
    with app.app_context():
        # 1. 获取或创建学院（通信与信息工程学院）
        college = College.query.filter_by(name="通信与信息工程学院").first()
        if not college:
            print("ERROR: 找不到通信与信息工程学院，请先确保 college 表中存在该学院")
            return
        college_id = college.id

        # 2. 获取或创建专业（数据科学与大数据技术）
        major = Major.query.filter_by(name="数据科学与大数据技术", college_id=college_id).first()
        if not major:
            major = Major(name="数据科学与大数据技术", college_id=college_id)
            db.session.add(major)
            db.session.commit()
            print(f"Created major: {major.name}")
        major_id = major.id

        # 3. 创建模块（按层级顺序，父模块先创建）
        module_map = {}
        for name, credits, parent_name in module_hierarchy:
            if name in module_map:
                continue
            parent_id = None
            if parent_name:
                parent_id = module_map.get(parent_name)
                if not parent_id:
                    print(f"Warning: 父模块 {parent_name} 不存在，跳过 {name}")
                    continue
            # 检查是否已存在（同一专业下）
            existing = Module.query.filter_by(name=name, major_id=major_id, college_id=college_id).first()
            if existing:
                module_map[name] = existing.id
                print(f"Module exists: {name}")
                continue
            mod = Module(
                name=name,
                required_credits=credits,
                parent_id=parent_id,
                college_id=college_id,
                major_id=major_id
            )
            db.session.add(mod)
            db.session.commit()
            module_map[name] = mod.id
            print(f"Created module: {name} ({credits} credits)")

        # 4. 插入课程
        inserted = 0
        skipped = 0
        for module_name, course_code, course_name, credit in course_list:
            module_id = module_map.get(module_name)
            if not module_id:
                print(f"Warning: 找不到模块 {module_name}，跳过课程 {course_name}")
                skipped += 1
                continue
            if course_code:
                existing = Course.query.filter_by(course_code=course_code, module_id=module_id).first()
                if existing:
                    print(f"Course exists: {course_code} {course_name}")
                    skipped += 1
                    continue
            else:
                existing = Course.query.filter_by(name=course_name, module_id=module_id).first()
                if existing:
                    print(f"Course exists (no code): {course_name}")
                    skipped += 1
                    continue
            course = Course(
                course_code=course_code,
                name=course_name,
                credit=credit,
                module_id=module_id
            )
            db.session.add(course)
            inserted += 1
            print(f"Added course: {course_name} ({credit} credits) -> {module_name}")

        db.session.commit()
        print(f"\nImport completed!")
        print(f"   Modules: {len(module_map)}")
        print(f"   Courses added: {inserted}, skipped: {skipped}")

if __name__ == "__main__":
    import_curriculum()